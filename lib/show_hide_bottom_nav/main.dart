class Home extends StatefulWidget {
  const Home({Key? key}) : super(key: key);

  @override
  State<Home> createState() => _HomeState();
}

class _HomeState extends State<Home> {
  late final PageController pageController;
  final ScrollController _scrollController = ScrollController();
  int pageNo = 0;

  Timer? carasouelTmer;

  Timer getTimer() {
    return Timer.periodic(const Duration(seconds: 3), (timer) {
      if (pageNo == 4) {
        pageNo = 0;
      }
      pageController.animateToPage(
        pageNo,
        duration: const Duration(seconds: 1),
        curve: Curves.easeInOutCirc,
      );
      pageNo++;
    });
  }

  @override
  void initState() {
    pageController = PageController(initialPage: 0, viewportFraction: 0.85);
    carasouelTmer = getTimer();
    _scrollController.addListener(() {
      if (_scrollController.position.userScrollDirection ==
          ScrollDirection.reverse) {
        showBtmAppBr = false;
        setState(() {});
      } else {
        showBtmAppBr = true;
        setState(() {});
      }
    });
    super.initState();
  }

  @override
  void dispose() {
    pageController.dispose();
    super.dispose();
  }

  bool showBtmAppBr = true;
  int currentPageIndex = 0;

  @override
  Widget build(BuildContext context) {
    return Consumer(
      builder: (_, WidgetRef ref, __) {
        final indexBottomNavbar = ref.watch(indexBottomNavbarProvider);
        return Scaffold(
          body: SafeArea(
            child: indexBottomNavbar == 0
                ? Center(
              child: ElevatedButton(
                onPressed: () {
                  Navigator.pushNamed(context, '/second');  // Navigate to SecondScreen
                },
                child: const Text('Launch SecondScreen'),
              ),
            )
                : bodies[indexBottomNavbar],
          ),
          bottomNavigationBar: AnimatedContainer(
            duration: const Duration(
              milliseconds: 800,
            ),
            curve: Curves.easeInOutSine,
            height: showBtmAppBr ? 70 : 0,
            child: BottomNavigationBar(
              items: const <BottomNavigationBarItem>[
                BottomNavigationBarItem(
                  icon: Icon(Icons.home),
                  label: 'Home',
                  backgroundColor: Colors.red,
                ),
                BottomNavigationBarItem(
                  icon: Icon(Icons.business),
                  label: 'Business',
                  backgroundColor: Colors.green,
                ),
                BottomNavigationBarItem(
                  icon: Icon(Icons.school),
                  label: 'School',
                  backgroundColor: Colors.purple,
                ),
                BottomNavigationBarItem(
                  icon: Icon(Icons.settings),
                  label: 'Settings',
                  backgroundColor: Colors.pink,
                ),
              ],
              currentIndex: indexBottomNavbar,
              selectedItemColor: Colors.amber[800],
              onTap: (value) {
                ref.read(indexBottomNavbarProvider.notifier).update((state) => value);
              },
            ),
          ),
        );
      },
    );
  }
}

